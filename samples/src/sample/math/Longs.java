package sample.math;

public class Longs {
    public static long add(long a, long b) {
        return a + b;
    }

    public long subtract(long a, long b) {
        return a - b;
    }

    public static long multiplyIntLong(int a, long b) {
        return a * b;
    }

    public long multiplyShortLong(short a, long b) {
        return a * b;
    }
}
